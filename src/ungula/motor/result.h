#pragma once

#include <cstdint>
#include <type_traits>
#include <utility>

namespace ungula::motor
{

/// Error codes returned by axis commands and queries.
///
/// `Ok` is the success value; everything else is a typed failure mode
/// surfaced to the caller. There are no `void`-returning commands that
/// can silently fail — every command goes through `Result<>` or `Status`.
enum class ErrorCode : uint8_t {
        Ok = 0,

        // Lifecycle
        NotInitialized,
        AlreadyInitialized,

        // Configuration
        InvalidConfig,
        Unsupported,

        // State
        InvalidState,
        NotEnabled,
        NotHomed,
        HomingRequired,
        MotionInProgress,
        LimitActive,

        // Faults
        DriverFault,
        TransportError,
        Timeout,

        // Resources
        QueueFull,

        InternalError,
};

/// Convert an `ErrorCode` to a short string for logging / diagnostics.
/// Implemented inline so it costs nothing if the call is optimized away.
inline const char *errorToString(ErrorCode e)
{
        switch (e) {
        case ErrorCode::Ok:
                return "Ok";
        case ErrorCode::NotInitialized:
                return "NotInitialized";
        case ErrorCode::AlreadyInitialized:
                return "AlreadyInitialized";
        case ErrorCode::InvalidConfig:
                return "InvalidConfig";
        case ErrorCode::Unsupported:
                return "Unsupported";
        case ErrorCode::InvalidState:
                return "InvalidState";
        case ErrorCode::NotEnabled:
                return "NotEnabled";
        case ErrorCode::NotHomed:
                return "NotHomed";
        case ErrorCode::HomingRequired:
                return "HomingRequired";
        case ErrorCode::MotionInProgress:
                return "MotionInProgress";
        case ErrorCode::LimitActive:
                return "LimitActive";
        case ErrorCode::DriverFault:
                return "DriverFault";
        case ErrorCode::TransportError:
                return "TransportError";
        case ErrorCode::Timeout:
                return "Timeout";
        case ErrorCode::QueueFull:
                return "QueueFull";
        case ErrorCode::InternalError:
                return "InternalError";
        }
        return "Unknown";
}

/// Result<T> — either `T` value or `ErrorCode`. `T = void` specialisation
/// is provided as `Status`.
///
/// Requirements on `T`:
///   - `T` must be default-constructible (the err path stores a
///     default-constructed `T` in the value slot — for move-only types
///     like `std::unique_ptr`, that's the null state).
///   - `T` must be move-constructible. Copy is NOT required —
///     `Result<std::unique_ptr<X>>` works.
///
/// Conventions:
///   - Default construction is `ErrorCode::InternalError` — never silently
///     succeeds. Use `Result<T>::Ok(value)` or `Result<T>::Err(code)`.
///   - `bool(r)` is true iff the result holds a value.
///   - `.value()` returns a reference for inspection; check `ok()` first.
///   - `.takeValue()` MOVES the value out and leaves the Result in a
///     moved-from state. After `takeValue()`, do not call `value()` or
///     `takeValue()` again. Designed for move-only `T`:
///
///       ```cpp
///       auto r = Axis::createStepDirStepper(cfg);
///       if (!r.ok()) return r.error();
///       std::unique_ptr<Axis> axis = r.takeValue();
///       axis->begin();
///       ```
///
///   - `valueOr()` requires `T` to be copyable. Instantiating it for a
///     move-only `T` is a compile error (by design — caller can't
///     supply a default for a unique_ptr without ambiguity).
template <typename T> class Result {
    public:
        // Static factories use `Ok` / `Err` to avoid clashing with
        // the `ok()` predicate.
        static Result Ok(T v)
        {
                return Result(std::move(v));
        }
        static Result Err(ErrorCode code)
        {
                return Result(code);
        }

        Result()
                : code_(ErrorCode::InternalError)
        {
        }

        // Move-only by default. Copy operations are implicitly deleted
        // whenever `T` is non-copyable; for copyable `T` they are
        // generated by the compiler. We keep move ops explicitly defaulted
        // so the intent is documented.
        Result(Result &&) = default;
        Result &operator=(Result &&) = default;
        Result(const Result &) = default;
        Result &operator=(const Result &) = default;

        bool ok() const
        {
                return code_ == ErrorCode::Ok;
        }
        explicit operator bool() const
        {
                return ok();
        }

        ErrorCode error() const
        {
                return code_;
        }
        const T &value() const &
        {
                return value_;
        }
        T &value() &
        {
                return value_;
        }

        /// Move the value out of the Result. After this call, the Result is
        /// in a moved-from state — calling `value()` or `takeValue()` again
        /// is undefined. Returns a default-constructed `T` if the Result
        /// holds an error (caller is responsible for checking `ok()` first).
        T takeValue()
        {
                return std::move(value_);
        }

        /// Copyable-T convenience. Compile error for move-only T — by
        /// design; callers of move-only Results use `takeValue()` with
        /// an explicit `ok()` check instead of a silent fallback.
        template <typename U = T> U valueOr(U fallback) const
        {
                return ok() ? value_ : std::move(fallback);
        }

    private:
        explicit Result(T v)
                : code_(ErrorCode::Ok)
                , value_(std::move(v))
        {
        }
        explicit Result(ErrorCode c)
                : code_(c)
                , value_{}
        {
        }

        ErrorCode code_;
        T value_;
};

/// `Status` is the void specialisation — just success or an error.
class Status {
    public:
        // See note on Result<T>::Ok / Err.
        static Status Ok()
        {
                return Status(ErrorCode::Ok);
        }
        static Status Err(ErrorCode code)
        {
                return Status(code);
        }

        Status()
                : code_(ErrorCode::InternalError)
        {
        }

        bool ok() const
        {
                return code_ == ErrorCode::Ok;
        }
        explicit operator bool() const
        {
                return ok();
        }
        ErrorCode error() const
        {
                return code_;
        }

    private:
        explicit Status(ErrorCode c)
                : code_(c)
        {
        }
        ErrorCode code_;
};

} // namespace ungula::motor
